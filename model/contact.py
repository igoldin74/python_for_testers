
class Contact:

    def __init__(self, firstname=None,
                 lastname=None,
                 middlename=None,
                 homephone=None,
                 mobilephone=None,
                 email=None):
        self.firstname = firstname
        self.lastname = lastname
        self.middlename = middlename
        self.homephone = homephone
        self.mobilephone = mobilephone
        self.email = email